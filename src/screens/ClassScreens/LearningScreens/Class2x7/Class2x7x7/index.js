import { View, Text, StyleSheet, ScrollView } from 'react-native'
import React, { useState, useEffect  } from 'react'
import { useFocusEffect } from "@react-navigation/native";
import ProgressBar from '../../../../../components/bars/progressBar'
import BottomBar from '../../../../../components/bars/bottomBar'
import generalStyles from '../../../../../styles/generalStyles';

const currentScreen = 7;  //current screen



const Class2x7x7 = ({route}) => { // name

  
    const {userPoints,  latestScreen, comeBackRoute, allScreensNum} = route.params
    
    

    const [currentPoints, setCurrentPoints] = useState(userPoints);
    const [latestScreenDone, setLatestScreenDone] = useState(currentScreen);
    const [comeBack, setComeBack] = useState(false);


    useFocusEffect(() => {
        if (latestScreen > currentScreen) {
            setLatestScreenDone(latestScreen);
            setComeBack(true)
        } 

        if (route.params.userPoints > 0) {
            setCurrentPoints(userPoints)
        }
    })


  return (
    <View style={styles.mainContainer}> 
        <ScrollView showsVerticalScrollIndicator={false} style={styles.body}>
          <View style={styles.textContainer}>
            <Text style={styles.text}><Text style={styles.textColor}>Setningsadverbial</Text> can be placed in different positions within a sentence, but they often come at the beginning or after the verb which is less common. {'\n\n'}Placing <Text style={styles.textColor}>adverbial</Text> at the end adds emphasis, making it seem like a kind of afterthought. {'\n\n'}The placement can sometimes affect the emphasis or focus of the sentence.</Text>
          </View>

          
          <View style={styles.exampleContainer}>
            <Text style={styles.exampleTextSmall}><Text style={styles.exampleTextSmallColor}>Dessverre</Text> kan jeg ikke komme.</Text>
            <Text style={styles.textSmall}>Unfortunately, I can't come.</Text>
          </View>

          <View style={styles.exampleContainer}>
          <Text style={styles.exampleTextSmall}>Jeg vet <Text style={styles.exampleTextSmallColor}>sikkert</Text> ikke.</Text>
            <Text style={styles.textSmall}>I certainly don't know.</Text>
          </View>

          <View style={styles.exampleContainer}>
            <Text style={styles.exampleTextSmall}>Hun kom i tide, <Text style={styles.exampleTextSmallColor}>heldigvis</Text>.</Text>
            <Text style={styles.textSmall}>She arrived on time, fortunately.</Text>
          </View>

        </ScrollView>

        <View style={styles.progressBarContainer}>

          <ProgressBar screenNum={currentScreen} totalLenghtNum={allScreensNum} latestScreen={latestScreenDone} comeBack={comeBackRoute}/>
        </View>

        <View style={styles.bottomBarContainer}>
          <BottomBar 
          linkNext={'Class2x7x8'} //link to next screen
          linkPrevious={'Class2x7x6'}  //link to previous screen
          buttonWidth={generalStyles.buttonNextPrevSize}
          buttonHeight={generalStyles.buttonNextPrevSize}
          userPoints={currentPoints}
          latestScreen={latestScreenDone}
          currentScreen={currentScreen}
          comeBack={comeBack}
          allScreensNum={allScreensNum}
          />
        </View>
    </View>
  )
}

export default Class2x7x7 //name for export

