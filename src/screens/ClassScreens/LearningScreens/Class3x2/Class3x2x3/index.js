import { View, Text, StyleSheet, ScrollView } from 'react-native'
import React, { useState, useEffect  } from 'react'
import { useFocusEffect } from "@react-navigation/native";
import ProgressBar from '../../../../../components/bars/progressBar'
import BottomBar from '../../../../../components/bars/bottomBar'
import generalStyles from '../../../../../styles/generalStyles';

const currentScreen = 3;  //current screen



const Class3x2x3 = ({route}) => { // name

  
    const {userPoints,  latestScreen, comeBackRoute, allScreensNum} = route.params
    
    

    const [currentPoints, setCurrentPoints] = useState(userPoints);
    const [latestScreenDone, setLatestScreenDone] = useState(currentScreen);
    const [comeBack, setComeBack] = useState(false);


    useFocusEffect(() => {
        if (latestScreen > currentScreen) {
            setLatestScreenDone(latestScreen);
            setComeBack(true)
        } 

        if (route.params.userPoints > 0) {
            setCurrentPoints(userPoints)
        }
    })


  return (
    <View style={styles.mainContainer}> 
        <ScrollView showsVerticalScrollIndicator={false} style={styles.body}>
          <View style={styles.textContainer}>
            

            <Text style={styles.text}>To form negative commands, you can use the word "ikke" (not) before the imperative verb form:</Text>
          </View>

          <View style={styles.exampleContainer}>
            <Text style={styles.exampleTextSmall}>å røre (to touch)</Text>
            <Text style={styles.textSmall}>Imperative: rør</Text>
            <Text style={styles.textSmall}>Ikke rør det! (Don't touch that!)</Text>
          </View>

          <View style={styles.exampleContainer}>
            <Text style={styles.exampleTextSmall}>å løpe (to run)</Text>
            <Text style={styles.textSmall}>Imperative: løp</Text>
            <Text style={styles.textSmall}>Ikke løp i gangene! (Don't run in the hallways!)</Text>
          </View>

          <View style={styles.exampleContainer}>
            <Text style={styles.exampleTextSmall}>kaste (to throw, but in this context it means to litter)</Text>
            <Text style={styles.textSmall}>Imperative: kast</Text>
            <Text style={styles.textSmall}>Ikke kast søppel på gaten! (Don't litter on the street!)</Text>
          </View>

          <View style={styles.exampleContainer}>
            <Text style={styles.exampleTextSmall}>å forstyrre (to disturb)</Text>
            <Text style={styles.textSmall}>Imperative: forstyrr</Text>
            <Text style={styles.textSmall}>Ikke forstyrr meg! (Don't disturb me!)</Text>
          </View>

          


        </ScrollView>

        <View style={styles.progressBarContainer}>

          <ProgressBar screenNum={currentScreen} totalLenghtNum={allScreensNum} latestScreen={latestScreenDone} comeBack={comeBackRoute}/>
        </View>

        <View style={styles.bottomBarContainer}>
          <BottomBar 
          linkNext={'Class3x2x4'} //link to next screen
          linkPrevious={'Class3x2x2'}  //link to previous screen
          buttonWidth={generalStyles.buttonNextPrevSize}
          buttonHeight={generalStyles.buttonNextPrevSize}
          userPoints={currentPoints}
          latestScreen={latestScreenDone}
          currentScreen={currentScreen}
          comeBack={comeBack}
          allScreensNum={allScreensNum}
          />
        </View>
    </View>
  )
}

export default Class3x2x3 //name for export

